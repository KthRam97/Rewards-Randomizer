for i=1,7 do
	local P3DFile = P3D.P3DChunk:new{Raw = ReadFile("/GameData/art/missions/level0"..i.."/level.p3d")}
	
	for j=1,#CarLocators[i] do
		local Locator = CarLocators[i][j]
		P3DFile:AddChunk(P3D.LocatorP3DChunk:createType3(Locator[1], {X=Locator[2],Y=Locator[3],Z=Locator[4]}, Locator[5]):Output())
	end
	
	for j=1,#CostumeLocators[i] do
		local Locator = CostumeLocators[i][j]
		local LocatorChunk = P3D.LocatorP3DChunk:createType9(Locator[1], {X=Locator[2],Y=Locator[3],Z=Locator[4]}, "PurchaseSkin", Locator[5], "<null>", 3, 0)
		LocatorChunk:AddChunk(P3D.TriggerVolumeP3DChunk:create(Locator[1] .. " Trigger",0,{X=2.5,Y=2.5,Z=2.5},{M11=0,M12=0,M13=0,M14=0,M21=0,M22=0,M23=0,M24=0,M31=0,M32=0,M33=0,M34=0,M41=Locator[2],M42=Locator[3],M43=Locator[4],M44=0}):Output())
		P3DFile:AddChunk(LocatorChunk:Output())
	end
	
	for idx in P3DFile:GetChunkIndexes(P3D.Identifiers.Locator) do
		local LocatorChunk = P3D.LocatorP3DChunk:new{Raw = P3DFile:GetChunkAtIndex(idx)}
		if P3D.CleanP3DString(LocatorChunk.Name) == "gil_loc" and LocatorChunk.Type == 3 then
			local Locator = GilLocators[i][1]
			P3DFile:SetChunkAtIndex(idx, P3D.LocatorP3DChunk:createType3(LocatorChunk.Name, {X=Locator[1],Y=Locator[2],Z=Locator[3]}, Locator[4]):Output())
		elseif P3D.CleanP3DString(LocatorChunk.Name) == "gil_car" and LocatorChunk.Type == 3 then
			local Locator = GilLocators[i][2]
			P3DFile:SetChunkAtIndex(idx, P3D.LocatorP3DChunk:createType3(LocatorChunk.Name, {X=Locator[1],Y=Locator[2],Z=Locator[3]}, Locator[4]):Output())
		end
	end
	
	if Settings.RandomCardLocations then
		local PossibleCards = {table.unpack(CardLocators[i])}
		for j=1,7 do
			local idx = math.random(#PossibleCards)
			local Locator = PossibleCards[idx]
			table.remove(PossibleCards, idx)
			local LocatorChunk = P3D.LocatorP3DChunk:createType9("card" .. i .. j, {X=Locator[1],Y=Locator[2],Z=Locator[3]}, "CollectorCard", "card" .. i .. j, "card" .. i .. j, 3, 0)
			LocatorChunk:AddChunk(P3D.TriggerVolumeP3DChunk:create("card" .. i .. j .. "Trigger",0,{X=2.5,Y=2.5,Z=2.5},{M11=0,M12=0,M13=0,M14=0,M21=0,M22=0,M23=0,M24=0,M31=0,M32=0,M33=0,M34=0,M41=Locator[1],M42=Locator[2],M43=Locator[3],M44=0}):Output())
			P3DFile:AddChunk(LocatorChunk:Output())
			Seed.AddSpoiler("L" .. i .. "C" .. j .. "|" .. Locator[4])
		end
	end
	
	Cache["Level" .. i] = P3DFile:Output()
end
